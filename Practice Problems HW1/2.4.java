//An Automobile consists of a model, price, mileage, 
// and boolean if it was used or not
class Automobile{
	String model;
	int price;
	double mileage;
	boolean used;

	Automobile(String model, int price, double mileage, boolean used) {
		this.model = model;
		this.price = price;
		this.mileage = mileage;
		this.used = used;
	}
}